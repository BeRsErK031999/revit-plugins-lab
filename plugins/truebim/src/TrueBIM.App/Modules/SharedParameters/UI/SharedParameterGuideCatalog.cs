namespace TrueBIM.App.Modules.SharedParameters.UI;

public enum SharedParameterGuideTopic
{
    Overview,
    ProjectUsageAnalysis,
    ProjectFamilyPresence,
    FamilyGuidCatalog,
    DeepFamilyAnalysis,
    SafeDeletion
}

public sealed record SharedParameterGuideSection(
    string Title,
    IReadOnlyList<string> Items);

public sealed record SharedParameterGuidePage(
    string Title,
    string Summary,
    IReadOnlyList<SharedParameterGuideSection> Sections);

public static class SharedParameterGuideCatalog
{
    public static IReadOnlyList<SharedParameterGuideTopic> ContextTopics { get; } =
    [
        SharedParameterGuideTopic.ProjectUsageAnalysis,
        SharedParameterGuideTopic.ProjectFamilyPresence,
        SharedParameterGuideTopic.FamilyGuidCatalog,
        SharedParameterGuideTopic.DeepFamilyAnalysis,
        SharedParameterGuideTopic.SafeDeletion
    ];

    public static SharedParameterGuidePage Get(SharedParameterGuideTopic topic)
    {
        return topic switch
        {
            SharedParameterGuideTopic.ProjectUsageAnalysis => CreateProjectUsageAnalysis(),
            SharedParameterGuideTopic.ProjectFamilyPresence => CreateProjectFamilyPresence(),
            SharedParameterGuideTopic.FamilyGuidCatalog => CreateFamilyGuidCatalog(),
            SharedParameterGuideTopic.DeepFamilyAnalysis => CreateDeepFamilyAnalysis(),
            SharedParameterGuideTopic.SafeDeletion => CreateSafeDeletion(),
            _ => CreateOverview()
        };
    }

